class CustomerService:

    @staticmethod
    def create():
        return 'Create CustomerService'

    @staticmethod
    def read_one():
        return 'Read One CustomerService'

    @staticmethod
    def read_many():
        return 'Read Many CustomerService'

    @staticmethod
    def update():
        return 'Update CustomerService'

    @staticmethod
    def delete():
        return 'Delete CustomerService'
